public class Letter {

    private char value;
    private int index;


    public Letter(char value) {
        this.value = value;
    }

    public char getValue() {
        return value;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public void setValue(char value) {
        this.value = value;
    }
}
